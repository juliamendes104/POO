
package com.mycompany.exemploreta;

public class Retangulo {
    private int b;
    private int a;
    
    public Retangulo(int b, int a){
        this.b = b;
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }
   
}
